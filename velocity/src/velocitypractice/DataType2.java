package velocitypractice;

public class DataType2 {
	
	public static void main(String[] args) {
		
		boolean IndiaPopulation = true;
		
		byte Area = 100;
		
		int City = 2000000000;
		
		short Town = 32500;
		
		long OverAllPopulation = 4586957920L;
		
		double AveragePopulation = 18.12365478951;
		
		float PopulationPercentage = 18.80f;
		
		char PopulationGrade = 'A';
		
		System.out.println(IndiaPopulation);
		System.out.println(Area);
		System.out.println(City);
		System.out.println(Town);
		System.out.println(OverAllPopulation);
		System.out.println(AveragePopulation);
		System.out.println(PopulationPercentage);
		System.out.println(PopulationGrade);
			
	}

}
